public class BonecoData {
    public String nome;
    public String raca;
    public String roule;
    public String arma;
    public int nivel;

    public BonecoData(String nome, String raca, String roule, String arma, int nivel) {
        this.nome = nome;
        this.raca = raca;
        this.roule = roule;
        this.arma = arma;
        this.nivel = nivel;
    }
}
